/**
 * @file 認証関連ページのローディング表示
 * @description 認証ページ（ログイン、新規登録など）の読み込み中に表示されるローディングコンポーネント
 *
 * 機能概要:
 * - ページ遷移時のローディング状態を視覚的に表示
 * - React Suspenseのフォールバックとして自動的に使用される
 * - 統一されたローディングUI体験を提供
 *
 * @remarks
 * Next.js App Routerでは、loading.tsxファイルを配置することで、
 * そのディレクトリ配下のページ読み込み中に自動的にこのコンポーネントが表示されます。
 *
 * このファイルは内部的にReact Suspenseのfallbackとして機能し、
 * ページコンポーネントの非同期処理（データフェッチなど）が完了するまで表示されます。
 *
 * サイズ「sm」を指定することで、認証ページに適した小さめのローディング表示にしています。
 */

// 共通のローディング画面コンポーネント
// アプリケーション全体で統一されたローディングUIを提供
import { LoadingScreen } from '@/components/common/LoadingScreen'

/**
 * 認証ページのローディングコンポーネント
 *
 * 処理内容:
 * - LoadingScreenコンポーネントを小サイズ（sm）でレンダリング
 * - ページデータの読み込み完了まで表示される
 *
 * @remarks
 * このコンポーネントはNext.jsによって自動的に呼び出されるため、
 * 明示的にインポートして使用する必要はありません。
 *
 * @returns ローディング画面のJSX要素
 */
export default function AuthLoading() {
  // 小サイズのローディング画面を表示
  // 認証ページは比較的シンプルなため、コンパクトな表示が適切
  return <LoadingScreen size="sm" />
}
